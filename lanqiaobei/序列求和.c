/*

求1+2+3+...+n的值。
输入格式
输入包括一个整数n。
输出格式
输出一行，包括一个整数，表示1+2+3+...+n的值。

数据规模与约定
1 <= n <= 1,000,000,000。
说明：请注意这里的数据规模。

本题直接的想法是直接使用一个循环来累加，然而，当数据规模很大时，这种“暴力”的方法往往会导致超时。此时你需要想想其他方法。你可以试一试，如果使用1000000000作为你的程序的输入，你的程序是不是能在规定的上面规定的时限内运行出来。

本题另一个要值得注意的地方是答案的大小不在你的语言默认的整型(int)范围内，如果使用整型来保存结果，会导致结果错误。

如果你使用C++或C语言而且准备使用printf输出结果，则你的格式字符串应该写成%I64d以输出long long类型的整数。
*/

#include <stdio.h>
int main()
{
	long long int n=0;
	long long int fina=0;
	scanf("%I64d",&n);
	fina=(1+n)*n/2;
	printf("%I64d",fina);
	return 0;
}
//注意问题:
//1.数据规模与约定 1 <= n <= 1,000,000,000。所以int型 包含不下去 2^31=2.14*10^9
//所以用long long int型
//2.long long int型  scanf和printf 用 %I64d
//3.1 <= n <= 1,000,000,000，当n很大时，用for循环暴力求解 1+2+3+... 耗时太大。直接可以(1+n)*n/2
